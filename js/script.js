const bankOfBaroda = {
  accountHolder: "Sohel khan",
  accountNumber: 29261234786754,
  accountType: "Saving",
  accountBalance: 0,
  accountBalanceLimit: 5000000,
  accountStatement: [],
  deposit(amount) {
    if (amount <= 0) {
      return "Amount is negative";
    } else if (amount > this.accountBalanceLimit) {
      return "Amount is greater than accountBalanceLimit";
    } else {
      this.accountBalance += amount;
      this.accountStatement.push({ type: "deposit", amount: amount });
      return `Deposited ${amount} successfully. New account balance is ${this.accountBalance}`;
    }
  },
  withdraw(amount) {
    if (amount <= 0) {
      return "Amount is negative";
    } else if (amount > this.accountBalanceLimit) {
      return "Amount is greater than accountBalanceLimit";
    } else if (amount > this.accountBalance) {
      return "Amount is greater than accountBalance";
    } else {
      this.accountBalance -= amount;
      this.accountStatement.push({ type: "withdraw", amount: amount });
      return `Withdrawn ${amount} successfully. New account balance is ${this.accountBalance}`;
    }
  },
  printAccountBalance() {
    return this.accountBalance;
  },
  printAccountStatement() {
    let totaltransactions = 0;
    if (this.accountStatement.length === 0) {
      return "No transactions found";
    }
    return this.accountStatement.map(transaction => {
      if (transaction.type === "deposit") {
        totaltransactions += transaction.amount
        return `Deposited ${transaction.amount}`;
      } else if (transaction.type === "withdraw") {
        totaltransactions += transaction.amount
        return `Withdrew ${transaction.amount}` , `total ${totaltransactions}`;
      }
      
    }).join("\n");
  },
};

console.log(bankOfBaroda.deposit(10500)); // Deposit 10500
console.log(bankOfBaroda.withdraw(100)); // Withdraw 100
console.log(bankOfBaroda.deposit(5000)); // Deposit 5000
console.log(bankOfBaroda.withdraw(2000)); // Withdraw 2000
console.log(bankOfBaroda.deposit(20000)); // Deposit 20000
console.log(bankOfBaroda.withdraw(4000)); // Withdraw 4000
